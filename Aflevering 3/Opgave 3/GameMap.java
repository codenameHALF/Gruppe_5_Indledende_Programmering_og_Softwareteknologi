import java.awt.*;

public class GameMap {
    public int outWidth;
    public int outHeight;
    public int inWidth;
    public int inHeight;

    public GameMap(int outWidth, int outHeight, int inWidth, int inHeight) {
        this.outWidth = outWidth;
        this.outHeight = outHeight;
        this.inWidth = inWidth;
        this.inHeight = inHeight;
    }
}
